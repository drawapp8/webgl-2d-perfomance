function ColorParser() {
}

ColorParser.parserColor = function(str) {
	if(!str) {
		return ColorParser.tansparentColor;
	}

	str = str.toLowerCase();
	color = ColorParser.stockColors[str];
	if(color) {
		return color;
	}
	
	color = ColorParser.parserHexColor(str);
	if(color) {
		ColorParser.stockColors[str] = color;
	}

	return color;
}

ColorParser.parserHexColor = function(str) {
	var color = {a:1.0};

	color.r = parseInt("0x"+str.substr(1, 2))/0xff;
	color.g = parseInt("0x"+str.substr(3, 2))/0xff;
	color.b = parseInt("0x"+str.substr(5, 2))/0xff;
	if(str.length > 8) {
		color.a = parseInt("0x"+str.substr(7, 2))/0xff;
	}

	return color;
}

